    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed

        int selectedRowIndex = tblCourseEnrolled.getSelectedRow();
        
        if(selectedRowIndex >=0){
            CourseEnrolled courseEnrolled = (CourseEnrolled) tblCourseEnrolled.getValueAt(selectedRowIndex, 0);
            txtCourseID.setText(courseEnrolled.getCourse().getCourseId());
            txtCourseName.setText(courseEnrolled.getCourse().getCourseName());
            txtCourseInstructor.setText(courseEnrolled.getCourse().getCourseInstructor());
            txtGrades.setText(courseEnrolled.getGradeEarned());
            txtAreaRemarks.setText(courseEnrolled.getRemarks());
        }  else{
            JOptionPane.showMessageDialog(this,"Please select the row first");
        } 
    }//GEN-LAST:event_btnViewActionPerformed

