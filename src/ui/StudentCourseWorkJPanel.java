    private void populateTable(){
        
        ArrayList<CourseEnrolled> coursesEnrolled = business.getStudentDirectory().findStudent(studentID).getCourseWork().getCourseEnrolledList();
       
        DefaultTableModel model = (DefaultTableModel) tblCourseEnrolled.getModel();
        model.setRowCount(0);
        
        if(coursesEnrolled!=null){
            for(CourseEnrolled course : coursesEnrolled){
            Object[] row = new Object[4];
            row[0] = course;
            row[1] = course.getCourse().getCourseName();
            row[2] = course.getCourse().getCourseInstructor();
            row[3] = course.getGradeEarned();
            model.addRow(row);
        } 
        }
        
    }
    
