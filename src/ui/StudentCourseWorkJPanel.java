    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String courseID = txtSearch.getText();
        Course course = business.getCourseDirectory().searchCourse(courseID);
        StudentProfile studentProfile = business.getStudentDirectory().findStudent(studentID);
        Course result = business.getCourseDirectory().searchCourse(txtSearch.getText());
        if(result == null){
            JOptionPane.showMessageDialog(this, "Course number you entered does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
        }else{

            DefaultTableModel model = (DefaultTableModel) tblCourseEnrolled.getModel();
            model.setRowCount(0);

            Object[] row = new Object[4];
            row[0] = result;
            row[1] = result.getCourseName();
            row[2] = result.getCourseInstructor();
            row[3] = studentProfile.getCourseWork().findCourseEnrolled(course).getGradeEarned();
            model.addRow(row);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

