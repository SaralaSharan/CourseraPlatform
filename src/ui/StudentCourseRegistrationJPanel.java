    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        Course result = business.getCourseDirectory().searchCourse(txtSearch.getText());
        if(result == null){
            JOptionPane.showMessageDialog(this, "Course number you entered does not exist", "Information", JOptionPane.INFORMATION_MESSAGE);
        }else{

            DefaultTableModel model = (DefaultTableModel) tblCourse.getModel();
            model.setRowCount(0);

            Object[] row = new Object[4];
            row[0] = result;
            row[1] = result.getCourseName();
            row[2] = result.getCourseInstructor();
            row[3] = result.getCourseTimings();
            model.addRow(row);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed

         
        String courseID = txtCourseID.getText();
        StudentProfile studentProfile = business.getStudentDirectory().findStudent(studentID);
        System.out.println("STUDENT PROFILE: "+studentProfile);
        CourseDirectory courseDirectory = business.getCourseDirectory();
        Course course = courseDirectory.searchCourse(courseID);
        course.addNewStudent(studentProfile);
        
        if(studentProfile.getCourseWork().findCourseEnrolled(course) != null){
            JOptionPane.showMessageDialog(this, "You've already enrolled to this course!");
        }   
        else{
            CourseEnrolled courseEnrolled = new CourseEnrolled(course,"","");
            ArrayList<CourseEnrolled> courseWork = studentProfile.addNewCourseWork(courseEnrolled);

            JOptionPane.showMessageDialog(this, "Congragulations!, You've been enrolled to this course");
            UserActivity userActitvity = new UserActivity(studentProfile.getPerson().getFirstName(), "ONLINE", Activity.STUDENT_ENROLLED_TO_COURSE, new Date());
            business.getUserActivityDirectory().addUserActivity(userActitvity);
            setVisibility(false); 
        }
        
    }//GEN-LAST:event_btnEnrollActionPerformed

