    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        
        setVisibility(true);
        int selectedRowIndex = tblCourse.getSelectedRow();
        
        if(selectedRowIndex >=0){
        Course course = (Course) tblCourse.getValueAt(selectedRowIndex, 0);
        txtCourseID.setText(course.getCourseId());
        txtCourseName.setText(course.getCourseName());
        txtCourseInstructor.setText(course.getCourseInstructor());
        txtCourseTimings.setText(course.getCourseTimings());
        txtCredits.setText(String.valueOf(course.getCreditsCarried()));
        
        String pid = course.getProfessorId();
        ProfessorProfile professor = business.getProfessorDirectory().findProfessor(pid);
        txtReputativeIndex.setText(String.valueOf(professor.getReputativeIndex()));

        }
        else{
            JOptionPane.showMessageDialog(this, "Please select a row a view");
            return;
        }
        
        populateTable();

    }//GEN-LAST:event_btnViewActionPerformed

