    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        
        int selectedRowIndex = tblCourse.getSelectedRow();
        
        if(selectedRowIndex >=0){
        CourseEnrolled courseEnrolled = (CourseEnrolled) tblCourse.getValueAt(selectedRowIndex, 0);    
        String professorID = courseEnrolled.getCourse().getProfessorId();
        
        ProfessorProfile professor = business.getProfessorDirectory().findProfessor(professorID);
        int repIndex = (professor.getReputativeIndex() == 0) ? 0 : professor.getReputativeIndex();
        int newRepIndex = Integer.parseInt(comboBoxRate.getSelectedItem().toString());
        int result = (repIndex+newRepIndex)/2;
        professor.setReputativeIndex(result);
        JOptionPane.showMessageDialog(this, "Thanks for your feedback!");
        populateTable();
        setVisibility(false);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

