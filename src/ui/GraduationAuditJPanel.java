     private void populateTable(){
        
        ArrayList<CourseEnrolled> coursesEnrolled = business.getStudentDirectory().findStudent(String.valueOf(studentID)).getCourseWork().getCourseEnrolledList();
        
        DefaultTableModel model = (DefaultTableModel) tblCourse.getModel();
        model.setRowCount(0);
        
        int grades=0;
        if(coursesEnrolled!=null){
            for(CourseEnrolled course : coursesEnrolled){
                
            String professorID = course.getCourse().getProfessorId();
            ProfessorProfile professor = business.getProfessorDirectory().findProfessor(professorID);
            
            Object[] row = new Object[5];
            row[0] = course;
            row[1] = course.getCourse().getCourseName();
            row[2] = course.getCourse().getCourseInstructor();
            row[3] = course.getGradeEarned();
            int repIndex = (professor.getReputativeIndex() == 0) ? 0 : professor.getReputativeIndex(); 
            row[4] = repIndex;
            model.addRow(row);
            grades+=course.getCourse().getCreditsCarried();
        } 
        }
        
        business.getStudentDirectory().findStudent(studentID).setTotalGradesEarned(grades);
        txtGrades.setText(String.valueOf(grades));
        if(grades<8){
            lblEvaluation.setText("You are lagging "+(8-grades)+" grades to graduate");
        }else{
            btnGraduate.setVisible(true);
        }
        
    }
     
     

